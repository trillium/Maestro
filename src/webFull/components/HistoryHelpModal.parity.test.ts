/**
 * Parity catalog — HistoryHelpModal
 *
 * Layer 2.5 — leaf-parade lift wave. Per WEB_PARITY_VERIFICATION (referenced
 * from ISA.md ISC-44.x), every feature port ships with a catalog of (Given,
 * When, Then) stories using the fixed assertion vocabulary:
 *
 *   hasElement, hasText, wsFrameMatches, dbHasRow, fsHas, processHas,
 *   notificationFired, broadcast
 *
 * HistoryHelpModal is a pure UI primitive — a presentational help/info modal
 * that explains the History panel feature set (Entry Types, Status Indicators,
 * Detail View, Resuming Sessions, Time & Cost, Activity Graph, Per-Session
 * Storage, Cross-Session Memory). It takes only `theme` and `onClose`. It
 * touches 0 IPC namespaces and 0 Electron-only APIs. The parity contract is
 * therefore observable-behavior-only: the modal renders with the right title,
 * shows each documented section with its key copy, exposes a single "Got it"
 * dismissal button, and closes via Escape / backdrop click / Got it button.
 * There are no side effects to assert beyond modal lifecycle.
 *
 * Catalog principle (from ISA Decisions 2026-06-08):
 * - The catalog IS the spec, not the renderer source.
 * - Pass criterion = every story passes on BOTH targets (Electron oracle at
 *   localhost:9222 and webFull at localhost:5176).
 * - Stories are layout-independent — they assert observable behavior, not
 *   DOM structure or CSS.
 */

import { describe, expect, it } from 'vitest';

/**
 * Allowed assertion verbs per WEB_PARITY_VERIFICATION. Adding a new verb here
 * is explicitly out of scope; if a story needs an assertion that doesn't fit,
 * the story is wrong, not the vocabulary.
 */
export type AssertionVerb =
	| 'hasElement'
	| 'hasText'
	| 'wsFrameMatches'
	| 'dbHasRow'
	| 'fsHas'
	| 'processHas'
	| 'notificationFired'
	| 'broadcast';

export interface Assertion {
	verb: AssertionVerb;
	target: string;
	value?: string;
}

export interface ParityStory {
	name: string;
	given: string;
	when: string[];
	then: Assertion[];
	happyPath: boolean;
}

